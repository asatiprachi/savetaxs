<h3>Limited Liability Partnership</h3>
<p>Limited Liability Partnership is governed by the Limited Liability Partnership Act 2008. 
    As the name suggests, gives a business the benefit of limited liability of a company and the flexibility of a partnership.  </p>
<h4>Benefits of Registering under Limited Liability Partnership</h4>
    <ul class="a">
        <li> The biggest advantage of LLP is that the partners of the LLP have limited liability, that means any partner is not liable to pay the debts of the company from their personal assets. </li>
        <li> Unlike forming a company, forming a LLP is an easy process and less time consuming with a minimum incorporating fees of Rs.500.</li>
        <li> Retirement, Insolvency or death of a partner does not affect the life of the LLP. It will get winded up only as per the provisions of the act. </li>
        <li> All decisions and management of the company are exercised by the directors of the company whereas shareholders receive very less power. </li>
        <li> In LLP there is no restriction upon admitting a partner into the company or leaving the firm or even transferring the ownership on others. </li>
    </ul>
