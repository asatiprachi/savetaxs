<h3>How to Register an LLP</h3>
<ul>
    <li><b>Step 1: </b> Obtain a Digital Signature Certificate (DSC).</li>
    <li class="Not">Before registering, each designated partner of the proposed LLP must 
    apply for digital signature because all the documents are needed to be submitted online. 
    These Digital Signature Certificates are to be obtained by recognized government certifying agencies only. </li>
    <li><b>Step 2: </b>Obtain Director Identification Number (DIN) </li>
    <li class="Not">All the designated partners or those who are intending to be a designated
     partner are required to apply for a DIN, the application of which has to be made in the Form DIR-3. 
     After attaching the scanned copy of documents (Aadhar and PAN) to the form, it shall be signed by a Company Secretary 
     in full-time employment or by the MD/Director/CEO/CFO of the existing company. </li>
    <li><b>Step 3: </b>Application for a Name</li>
    <li class="Not">The registered name should be unique and should not clash with an existing company name.
     This could be verified on the MCA portal. The name should not match with any of the existing partnership firms,
      LLPs, trademarks or body corporates. </li>
    <li><b>Step 4: </b>Incorporation of LLP</li>
    <li class="Not">1. The form used for incorporation is FiLLiP(Form for incorporation of Limited Liability Partnership) which
     shall be filed with the Registrar who has a jurisdiction over the state in which the registered office of the LLP is situated.
      The form will be an integrated form.</li>
    <li class="Not">2.	Fees as per Annexure ‘A’ shall be paid.</li>
    <li class="Not">3.	This form also provides for applying for allotment of DPIN, if an individual who is to be appointed as 
    a designated partner does not have a DPIN or DIN.</li>
    <li class="Not">4.	The application for allotment shall be allowed to be made by two individuals only.</li>
    <li class="Not">5.	The application for reservation may be made through FiLLiP too.</li>
<li class="Not">6.	If the name that is applied for is approved, then this approved and reserved name shall be filled as the proposed name of the LLP</li>
<li><b>Step 5: </b>Limited Liability Partnership Agreement</li>
<p>LLP agreement governs the mutual rights and duties amongst the partners and also between the LLP and its partners.</p>
<li class="Not">1. LLP agreement must be filed in form 3 online on MCA Portal.</li>
<li class="Not">2. Form 3 for LLP agreement has to be filed within 30 days of the date of incorporation.</li>
<li class="Not">3. The LLP Agreement has to be printed on Stamp Paper. The value of Stamp Paper is different for every state.</li>

</ul>
